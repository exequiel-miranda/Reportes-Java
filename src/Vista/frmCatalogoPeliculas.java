    private void btnMostrarReporteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarReporteActionPerformed

        try {
            JasperReport report = (JasperReport) JRLoader.loadObject(getClass().getResource("/Vista/ReporteEjemplo.jasper"));
            JasperPrint jprint = JasperFillManager.fillReport(report, null, ClaseConexion.getConexion());

            JasperViewer view = new JasperViewer(jprint, false);
            view.setTitle("Nombre Reporte");
            view.setDefaultCloseOperation(WindowConstants.DISPOSE_ON_CLOSE);
            view.setVisible(true);
        } catch (Exception e) {
            System.out.println("Error al mostrar el reporte" + e);
        }
    }//GEN-LAST:event_btnMostrarReporteActionPerformed

